package step01;
public class Exam04 {

    public static void main(String[] args) {
        System.out.println(10); // number
        System.out.println(+10); //
        System.out.println(-10); // 음수표현 시 -를 붙여야한다.
        System.out.println(3.14); // (31.4 * 10^-1) == 31.4e-1
        System.out.println(31.4 * (10^-1));
        System.out.println(31.4e-1);

        System.out.println("문자열"); // 문자열
        System.out.println('가'); // 단일 문자

        System.out.println(true); // 논리 - True
        System.out.println(false); // 논리 - false , 논리는 대소문자 구분한다.
    }
}