package step01;
public class Exam06_1
 {

    public static void main(String[] args) {
        //exponential expression = 숫자e지수 또는 숫자E지수
        System.out.println(0.0314e2); // 0.0314 * 10^2
        System.out.println(0.314e1); // 0.0314 * 10^2
        System.out.println(31.4e-1); // 0.0314 * 10^2
        System.out.println(314e-2); // 0.0314 * 10^2


    }
}